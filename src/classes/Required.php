<?php


namespace DR2GSistemas\korm\classes;

use Attribute;


#[Attribute(Attribute::TARGET_PROPERTY)]
class Required
{
    private $name;

    public function __construct(string $name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName(string $name): void
    {
        $this->name = $name;
    }


}
